
public class Account {
	

	
		private String names;
		private int balance;
		public String getNames() {
			return names;
		}
		public void setNames(String names) {
			this.names = names;
		}
		public int getBalance() {
			return balance;
		}
		public void setBalance(int balance) {
			this.balance = balance;
		}
		

}
